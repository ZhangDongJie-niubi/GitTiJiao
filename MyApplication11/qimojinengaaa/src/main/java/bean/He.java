package bean;

public class He {
    private long a;
    private long l;

    public He(long a, long l) {
        this.a = a;
        this.l = l;
    }

    public long getA() {
        return a;
    }

    public void setA(long a) {
        this.a = a;
    }

    public long getL() {
        return l;
    }

    public void setL(long l) {
        this.l = l;
    }
}

