package bean;

public class EvenMeg {
    private long l;
    private long a;

    public EvenMeg(long l, long a) {
        this.l = l;
        this.a = a;
    }

    public long getL() {
        return l;
    }

    public void setL(long l) {
        this.l = l;
    }

    public long getA() {
        return a;
    }

    public void setA(long a) {
        this.a = a;
    }
}
